/**
 * Get translation for a key
 * @param {string} key 
 * @returns {string}
 */
function t(key) {
    const lang = localStorage.getItem('lang') || 'en';
    if (typeof key === 'string') {
        const cleanKey = key.trim();
        if (lang === 'en') {
            // Regex match thông báo lỗi nạp tiền từ backend:
            const balanceRegex = /Số dư tài khoản không đủ!\s*Mỗi lượt tạo tạp chí cần\s*([\d,.]+)\s*(?:VND|đ)?\.\s*Số dư hiện tại của bạn là\s*([\d,.]+)\s*(?:VND|đ)?\.\s*Vui lòng nạp thêm tiền\./i;
            const match = cleanKey.match(balanceRegex);
            if (match) {
                const pricing = parseInt(match[1].replace(/[,.]/g, '')) || 0;
                const balance = parseInt(match[2].replace(/[,.]/g, '')) || 0;
                const pUsd = (pricing / 25000).toFixed(2);
                const bUsd = (balance / 25000).toFixed(2);
                return `Insufficient balance! Each magazine creation requires $${pUsd} USD (${pricing.toLocaleString('en-US')} VND). Your current balance is $${bUsd} USD (${balance.toLocaleString('en-US')} VND). Please top up.`;
            }

            // Chào mừng Admin [Tên]! -> Welcome Admin [Tên]!
            const welcomeAdminRegex = /Chào mừng Admin (.+)!/i;
            const welcomeAdminMatch = cleanKey.match(welcomeAdminRegex);
            if (welcomeAdminMatch) {
                let name = welcomeAdminMatch[1];
                if (name === "Quản trị viên") name = "Administrator";
                return `Welcome Admin ${name}!`;
            }

            // Chào mừng trở lại, [Tên]! -> Welcome back, [Tên]!
            const welcomeBackRegex = /Chào mừng trở lại,\s*(.+)!/i;
            const welcomeBackMatch = cleanKey.match(welcomeBackRegex);
            if (welcomeBackMatch) {
                let name = welcomeBackMatch[1];
                if (name === "Quản trị viên") name = "Administrator";
                return `Welcome back, ${name}!`;
            }

            // Chào mừng, [Tên]! -> Welcome, [Tên]!
            const welcomeRegex = /Chào mừng,\s*(.+)!/i;
            const welcomeMatch = cleanKey.match(welcomeRegex);
            if (welcomeMatch) {
                let name = welcomeMatch[1];
                if (name === "Quản trị viên") name = "Administrator";
                return `Welcome, ${name}!`;
            }

            // Lỗi kết nối Google: [Lỗi] -> Google connection error: [Lỗi]
            const googleConnRegex = /Lỗi kết nối Google:\s*(.+)/i;
            const googleConnMatch = cleanKey.match(googleConnRegex);
            if (googleConnMatch) {
                return `Google connection error: ${googleConnMatch[1]}`;
            }

            // Lỗi hệ thống: [Lỗi] -> System error: [Lỗi]
            const systemErrorRegex = /Lỗi hệ thống:\s*(.+)/i;
            const systemErrorMatch = cleanKey.match(systemErrorRegex);
            if (systemErrorMatch) {
                return `System error: ${systemErrorMatch[1]}`;
            }
        } else if (lang === 'vi') {
            const balanceRegexEn = /Insufficient balance!\s*Each magazine creation requires\s*\$?([\d,.]+)\s*(?:USD)?\s*\(([\d,.]+)\s*VND\)\.\s*Your current balance is\s*\$?([\d,.]+)\s*(?:USD)?\s*\(([\d,.]+)\s*VND\)\.\s*Please top up\./i;
            const match = cleanKey.match(balanceRegexEn);
            if (match) {
                const pricing = match[2];
                const balance = match[4];
                return `Số dư tài khoản không đủ! Mỗi lượt tạo tạp chí cần ${pricing} VND. Số dư hiện tại của bạn là ${balance} VND. Vui lòng nạp thêm tiền.`;
            }

            // Welcome Admin [Tên]! -> Chào mừng Admin [Tên]!
            const welcomeAdminRegexEn = /Welcome Admin (.+)!/i;
            const welcomeAdminMatchEn = cleanKey.match(welcomeAdminRegexEn);
            if (welcomeAdminMatchEn) {
                let name = welcomeAdminMatchEn[1];
                if (name === "Administrator") name = "Quản trị viên";
                return `Chào mừng Admin ${name}!`;
            }

            // Welcome back, [Tên]! -> Chào mừng trở lại, [Tên]!
            const welcomeBackRegexEn = /Welcome back,\s*(.+)!/i;
            const welcomeBackMatchEn = cleanKey.match(welcomeBackRegexEn);
            if (welcomeBackMatchEn) {
                let name = welcomeBackMatchEn[1];
                if (name === "Administrator") name = "Quản trị viên";
                return `Chào mừng trở lại, ${name}!`;
            }

            // Welcome, [Tên]! -> Chào mừng, [Tên]!
            const welcomeRegexEn = /Welcome,\s*(.+)!/i;
            const welcomeMatchEn = cleanKey.match(welcomeRegexEn);
            if (welcomeMatchEn) {
                let name = welcomeMatchEn[1];
                if (name === "Administrator") name = "Quản trị viên";
                return `Chào mừng, ${name}!`;
            }

            // Google connection error: [Lỗi] -> Lỗi kết nối Google: [Lỗi]
            const googleConnRegexEn = /Google connection error:\s*(.+)/i;
            const googleConnMatchEn = cleanKey.match(googleConnRegexEn);
            if (googleConnMatchEn) {
                return `Lỗi kết nối Google: ${googleConnMatchEn[1]}`;
            }

            // System error: [Lỗi] -> Lỗi hệ thống: [Lỗi]
            const systemErrorRegexEn = /System error:\s*(.+)/i;
            const systemErrorMatchEn = cleanKey.match(systemErrorRegexEn);
            if (systemErrorMatchEn) {
                return `Lỗi hệ thống: ${systemErrorMatchEn[1]}`;
            }
        }
    }
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    return key;
}

function applyTranslations(lang) {
    const isVi = lang === 'vi';
    
    // Always update UI elements like flags and currency regardless of language
    updateUI(isVi);

    const langDict = translations[lang] || {};
    const sortedKeys = Object.keys(langDict).sort((a, b) => b.length - a.length);

    // First handle data-i18n attributes
    // Handle data-i18n-title attributes for tooltips
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (!el.hasAttribute('data-original-title')) {
            el.setAttribute('data-original-title', el.getAttribute('title') || '');
        }
        const originalTitle = el.getAttribute('data-original-title');
        const translatedTitle = langDict[key] || langDict[originalTitle] || originalTitle || key;
        el.setAttribute('title', translatedTitle);
    });

    // Handle data-label attributes (used in responsive tables)
    document.querySelectorAll('[data-label]').forEach(el => {
        const key = el.getAttribute('data-label');
        if (!el.hasAttribute('data-original-label')) {
            el.setAttribute('data-original-label', key);
        }
        const originalLabel = el.getAttribute('data-original-label');
        const translatedLabel = langDict[key] || langDict[originalLabel] || originalLabel || key;
        el.setAttribute('data-label', translatedLabel);
    });

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');

        // Lưu văn bản gốc HTML (tiếng Anh) vào data-original-text trước lần dịch đầu tiên
        // Khi không có bản dịch, phục hồi gốc thay vì hiển thị key string
        if (!el.hasAttribute('data-original-text')) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.setAttribute('data-original-text', el.getAttribute('placeholder') || '');
            } else {
                el.setAttribute('data-original-text', el.textContent);
            }
        }

        const originalText = el.getAttribute('data-original-text');
        const translatedVal = langDict[key] || originalText || key;

        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            if (el.hasAttribute('placeholder')) el.setAttribute('placeholder', translatedVal);
        } else {
            el.textContent = translatedVal;
        }
    });

    const translateTextNodes = (node) => {
        if (node.nodeType === 3) {
            if (node.originalText === undefined) {
                node.originalText = node.nodeValue;
            }
            let originalText = node.originalText;
            let trimmedText = originalText.replace(/\s+/g, ' ').trim();
            
            if (trimmedText.length > 0) {
                // Try translation function first to support dynamic regex
                const translatedVal = t(trimmedText);
                if (translatedVal !== trimmedText) {
                    node.nodeValue = originalText.replace(trimmedText, translatedVal);
                    return;
                }
                
                // Longest prefix match if exact match fails
                let translated = false;
                for (let key of sortedKeys) {
                    if (trimmedText === key) {
                        node.nodeValue = originalText.replace(trimmedText, langDict[key]);
                        translated = true;
                        break;
                    }
                }
                
                if (!translated) {
                    node.nodeValue = originalText;
                }
            }
        } else if (node.nodeType === 1) {
            if (['SCRIPT', 'STYLE', 'CODE', 'TEXTAREA'].includes(node.tagName)) return;
            if (node.hasAttribute('data-i18n')) {
                // Already handled
                return;
            }

            if (node.hasAttribute('placeholder')) {
                if (!node.hasAttribute('data-org-placeholder')) {
                    node.setAttribute('data-org-placeholder', node.getAttribute('placeholder'));
                }
                let originalPlaceholder = node.getAttribute('data-org-placeholder').trim();
                let translatedPlaceholder = langDict[originalPlaceholder] || originalPlaceholder;
                node.setAttribute('placeholder', translatedPlaceholder);
            }

            node.childNodes.forEach(translateTextNodes);
        }
    };

    translateTextNodes(document.body);

    if (typeof window.onLanguageChanged === 'function') {
        window.onLanguageChanged(lang);
    }
}

function updateUI(isVi) {
    // Currency Conversion
    document.querySelectorAll('.currency-convert').forEach(el => {
        const raw = parseInt(el.getAttribute('data-balance')) || 0;
        if (isVi) {
            el.textContent = raw.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".") + ' VND';
        } else {
            const usd = (raw / 25000).toFixed(2);
            el.textContent = '$' + usd + ' USD';
        }
    });

    // Update currency unit labels
    document.querySelectorAll('.currency-unit').forEach(el => {
        el.textContent = isVi ? 'VND' : 'USD';
    });

    const langBtn = document.getElementById('langToggleText');
    const langFlag = document.getElementById('langFlag');
    if (langBtn && langFlag) {
        langBtn.innerText = isVi ? 'VN' : 'EN';
        langFlag.src = isVi ? 'https://flagcdn.com/w20/vn.png' : 'https://flagcdn.com/w20/us.png';
    }

    // Cập nhật các nút ngôn ngữ trên mobile drawer
    document.querySelectorAll('.lang-text-mobile').forEach(el => {
        el.innerText = isVi ? 'VN' : 'EN';
    });
    document.querySelectorAll('.lang-flag-mobile').forEach(el => {
        el.src = isVi ? 'https://flagcdn.com/w20/vn.png' : 'https://flagcdn.com/w20/us.png';
    });
}

function toggleLanguage() {
    const current = localStorage.getItem('lang') || 'en';
    const next = current === 'en' ? 'vi' : 'en';
    localStorage.setItem('lang', next);
    document.cookie = "lang=" + next + "; path=/; max-age=31536000; SameSite=Lax";
    
    // Apply translations to the current document
    applyTranslations(next);
    
    // Propagate language to parent if embedded (standard direct call for same-origin)
    try {
        if (window.parent && window.parent !== window && typeof window.parent.applyTranslations === 'function') {
            window.parent.applyTranslations(next);
        }
    } catch (e) {
        console.error("Could not propagate language to parent:", e);
    }
    
    // Propagate language to parent via HTML5 postMessage (safe cross-origin standard)
    try {
        if (window.parent && window.parent !== window) {
            window.parent.postMessage({ type: 'setLanguage', lang: next }, '*');
        }
    } catch (e) {
        console.error("Could not postMessage language to parent:", e);
    }
    
    // Propagate language to all child iframes (standard direct call)
    document.querySelectorAll('iframe').forEach(iframe => {
        try {
            if (iframe.contentWindow && typeof iframe.contentWindow.applyTranslations === 'function') {
                iframe.contentWindow.applyTranslations(next);
            }
        } catch (e) {
            console.error("Could not propagate language to iframe:", e);
        }
        // Propagate via postMessage (safe cross-origin)
        try {
            if (iframe.contentWindow) {
                iframe.contentWindow.postMessage({ type: 'setLanguage', lang: next }, '*');
            }
        } catch (e) {
            console.error("Could not postMessage language to iframe:", e);
        }
    });
}

function initLang() {
    // Khôi phục ngôn ngữ đã chọn từ localStorage hoặc cookie, mặc định là 'en'
    let lang = localStorage.getItem('lang');
    if (!lang) {
        // Thử lấy từ cookie
        const match = document.cookie.match(new RegExp('(^| )lang=([^;]+)'));
        if (match) {
            lang = match[2];
        }
    }
    if (!lang || (lang !== 'en' && lang !== 'vi')) {
        lang = 'en';
    }
    localStorage.setItem('lang', lang);
    document.cookie = "lang=" + lang + "; path=/; max-age=31536000; SameSite=Lax";
    applyTranslations(lang);
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLang);
} else {
    initLang();
}

// Safe Cross-Origin Message Listener for Global i18n Sync
window.addEventListener('message', function(event) {
    if (event.data && event.data.type === 'setLanguage') {
        const newLang = event.data.lang;
        if (newLang === 'en' || newLang === 'vi') {
            const currentLang = localStorage.getItem('lang');
            if (currentLang !== newLang) {
                localStorage.setItem('lang', newLang);
                document.cookie = "lang=" + newLang + "; path=/; max-age=31536000; SameSite=Lax";
                applyTranslations(newLang);
            }
        }
    }
});
